import * as React from "react"

import { cn } from "@/lib/utils"

/**
 * Card — Restyled to Uber Design System
 *
 * - White bg, 1px border #EEEEEE, border-radius 12px (uber-md)
 * - No shadow by default (Uber is flat / border-based)
 * - Hover: border goes black, translateY(-2px), add shadow-uber-card
 * - Transition: 200ms uber easing
 * - Padding: 20px (uber-5 = 24px closest, using p-5)
 */

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-uber-md border border-uber-gray-100 bg-white text-uber-black transition-all duration-uber-base ease-uber",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

